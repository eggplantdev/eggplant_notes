#!/usr/bin/env node
// DEV-ONLY fixture generator for the Load/Clear sample-data feature (S-12). NOT part of any
// build; never runs on CI/Vercel. Independent of generate-section-seed.mjs (which only parses
// markdown into seed.sql) — this reads the ALREADY-SEEDED local DB and emits a typed TS fixture.
//
// Why a committed fixture: seed.sql only runs on `supabase db reset` locally, so prod has no
// template account to copy from. This script captures the test@gmail.com corpus into
// src/features/sample-data/sample-data.ts, which ships in the prod bundle; the loader inserts it
// under the current user via the RLS client.
//
// REGENERATE WORKFLOW (the fixture is generated, never hand-edited):
//   1. edit seed content (seed.sql / generate-section-seed.mjs)  — NOT this file, NOT the fixture
//   2. supabase db reset            — re-seed the local DB with the new content
//   3. pnpm seed:dump-fixture       — runs this script, rewrites sample-data.ts
//   4. commit the regenerated sample-data.ts
//
// Usage (from repo root):  pnpm seed:dump-fixture
// Connects to the local stack via keys read from `supabase status -o env` (deterministic local
// JWTs), so no secrets are baked in and key rotation is handled automatically.

import { execSync } from 'node:child_process'
import { writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

import { createClient } from '@supabase/supabase-js'

const TEMPLATE_USER_ID = 'eeeeeeee-eeee-eeee-eeee-eeeeeeeeeeee' // test@gmail.com playground

const scriptDir = dirname(fileURLToPath(import.meta.url))
const repoRoot = resolve(scriptDir, '..', '..')
const outFile = resolve(repoRoot, 'src/features/sample-data/sample-data.ts')

// Pull local stack credentials from the CLI rather than hardcoding — the local service-role JWT
// is deterministic but reading it live survives any future key change.
function localCredentials() {
  const env = execSync('supabase status -o env', { cwd: repoRoot, encoding: 'utf8' })
  const read = (key) => env.match(new RegExp(`^${key}="?([^"\\n]+)"?$`, 'm'))?.[1]
  const url = read('API_URL')
  const serviceRoleKey = read('SERVICE_ROLE_KEY')
  if (!url || !serviceRoleKey) {
    throw new Error('Could not read API_URL / SERVICE_ROLE_KEY from `supabase status -o env`. Is the local stack running (`supabase start`)?')
  }
  return { url, serviceRoleKey }
}

async function main() {
  const { url, serviceRoleKey } = localCredentials()
  // Service-role bypasses RLS so we can read the template account's rows from a script context.
  const supabase = createClient(url, serviceRoleKey, { auth: { persistSession: false } })

  const [subjectsRes, notesRes, cardsRes] = await Promise.all([
    supabase
      .from('subjects')
      .select('id, title, description')
      .eq('user_id', TEMPLATE_USER_ID)
      .order('title'),
    supabase
      .from('notes')
      .select('id, subject_id, title, content, position')
      .eq('user_id', TEMPLATE_USER_ID)
      .order('subject_id', { nullsFirst: true })
      .order('position'),
    supabase
      .from('memory_cards')
      .select('id, note_id, prompt, example, code_context')
      .eq('user_id', TEMPLATE_USER_ID)
      .order('note_id')
      .order('id'),
  ])

  for (const [name, res] of [
    ['subjects', subjectsRes],
    ['notes', notesRes],
    ['memory_cards', cardsRes],
  ]) {
    if (res.error) throw new Error(`Failed reading ${name}: ${res.error.message}`)
  }

  if (subjectsRes.data.length === 0 && notesRes.data.length === 0) {
    throw new Error(`Template account ${TEMPLATE_USER_ID} has no rows. Run \`supabase db reset\` to seed it first.`)
  }

  const fixture = {
    subjects: subjectsRes.data.map((s) => ({
      ref: s.id,
      title: s.title,
      description: s.description,
    })),
    notes: notesRes.data.map((n) => ({
      ref: n.id,
      subjectRef: n.subject_id,
      title: n.title,
      content: n.content,
      position: n.position,
    })),
    cards: cardsRes.data.map((c) => ({
      noteRef: c.note_id,
      prompt: c.prompt,
      example: c.example,
      codeContext: c.code_context,
    })),
  }

  const banner =
    '// GENERATED by supabase/seed-scripts/dump-sample-fixture.mjs — DO NOT EDIT BY HAND.\n' +
    '// Regenerate: edit seed content → `supabase db reset` → `pnpm seed:dump-fixture` → commit.\n' +
    '// This is the prod-shippable copy of the test@gmail.com corpus; the loader inserts it under\n' +
    '// the current user. Source of truth stays supabase/seed.sql (never modified by this script).\n'

  const body = `import type { SampleDataT } from './types'\n\nexport const SAMPLE_DATA: SampleDataT = ${JSON.stringify(fixture, null, 2)}\n`

  writeFileSync(outFile, `${banner}\n${body}`)

  console.log(
    `Wrote ${outFile}\n  subjects: ${fixture.subjects.length}\n  notes:    ${fixture.notes.length}\n  cards:    ${fixture.cards.length}`,
  )
}

main().catch((err) => {
  console.error(err.message ?? err)
  process.exit(1)
})
