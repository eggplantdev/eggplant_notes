// Generates iOS PWA launch screens (apple-touch-startup-image) + the matching media-query metadata.
//
// WHY this exists: iOS standalone PWAs ignore the web-app-manifest `background_color`, so without
// per-device startup images iOS paints a WHITE launch/relaunch screen before the app renders — the
// PWA-only white flash. Android/desktop read the manifest and are already brand-dark.
//
// Output:
//   public/splash/splash-<W>x<H>.png   — solid BRAND_DARK rectangles (one per unique pixel size)
//   src/app/apple-splash.ts            — the startupImage[] array (url + exact device media query)
//
// Re-run after changing the device matrix or BRAND_DARK:  node scripts/gen-apple-splash.mjs
// Requires ImageMagick (`magick`) on PATH — host tool, not an npm dep.

import { execFileSync } from 'node:child_process'
import { mkdirSync, writeFileSync, readFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, resolve } from 'node:path'

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const SPLASH_DIR = resolve(ROOT, 'public/splash')

// Single source for the colour — read the literal out of brand-colors.ts so this can't drift from it.
const brandColors = readFileSync(resolve(ROOT, 'src/components/brand/brand-colors.ts'), 'utf8')
const BRAND_DARK = brandColors.match(/BRAND_DARK\s*=\s*'(#[0-9a-fA-F]{3,8})'/)?.[1]
if (!BRAND_DARK) throw new Error('Could not read BRAND_DARK from brand-colors.ts')

// Logical (CSS) px portrait dimensions + device-pixel-ratio per iOS device family. iOS matches a
// startup image by an EXACT media query on these logical sizes + ratio, so the matrix must be precise
// or the image is silently ignored. Devices sharing a logical size+ratio share one media query.
const DEVICES = [
  { w: 320, h: 568, r: 2 }, // SE 1st gen, 5/5s/SE
  { w: 375, h: 667, r: 2 }, // 8, 7, 6s, SE 2nd/3rd
  { w: 414, h: 736, r: 3 }, // 8/7/6s Plus
  { w: 375, h: 812, r: 3 }, // X, XS, 11 Pro, 12 mini, 13 mini
  { w: 414, h: 896, r: 2 }, // XR, 11
  { w: 414, h: 896, r: 3 }, // XS Max, 11 Pro Max
  { w: 390, h: 844, r: 3 }, // 12, 12 Pro, 13, 13 Pro, 14
  { w: 428, h: 926, r: 3 }, // 12/13 Pro Max, 14 Plus
  { w: 393, h: 852, r: 3 }, // 14 Pro, 15, 15 Pro, 16
  { w: 430, h: 932, r: 3 }, // 14 Pro Max, 15 Plus, 15 Pro Max, 16 Plus
  { w: 402, h: 874, r: 3 }, // 16 Pro
  { w: 440, h: 956, r: 3 }, // 16 Pro Max
  { w: 744, h: 1133, r: 2 }, // iPad mini 6
  { w: 768, h: 1024, r: 2 }, // iPad 9.7", mini/Air older
  { w: 810, h: 1080, r: 2 }, // iPad 10.2"
  { w: 820, h: 1180, r: 2 }, // iPad Air 10.9"
  { w: 834, h: 1112, r: 2 }, // iPad Pro 10.5"
  { w: 834, h: 1194, r: 2 }, // iPad Pro 11"
  { w: 1024, h: 1366, r: 2 }, // iPad Pro 12.9"
]

mkdirSync(SPLASH_DIR, { recursive: true })

const made = new Set()
const entries = []

for (const { w, h, r } of DEVICES) {
  for (const orientation of ['portrait', 'landscape']) {
    const lw = orientation === 'portrait' ? w : h
    const lh = orientation === 'portrait' ? h : w
    const pw = lw * r
    const ph = lh * r
    const file = `splash-${pw}x${ph}.png`
    if (!made.has(file)) {
      execFileSync('magick', ['-size', `${pw}x${ph}`, `xc:${BRAND_DARK}`, resolve(SPLASH_DIR, file)])
      made.add(file)
    }
    entries.push({
      url: `/splash/${file}`,
      media: `(device-width: ${lw}px) and (device-height: ${lh}px) and (-webkit-device-pixel-ratio: ${r}) and (orientation: ${orientation})`,
    })
  }
}

const ts = `// GENERATED by scripts/gen-apple-splash.mjs — do not edit by hand. Re-run the script to regenerate.
// iOS PWA launch screens: solid ${BRAND_DARK} splashes matched per device so iOS never paints its
// default white launch screen (the PWA-only white flash). Consumed by appleWebApp.startupImage in
// src/app/layout.tsx. See the script header for why iOS needs this and Android/desktop don't.

export const APPLE_SPLASH_SCREENS = ${JSON.stringify(entries, null, 2)} as const
`

writeFileSync(resolve(ROOT, 'src/app/apple-splash.ts'), ts)
console.log(`Generated ${made.size} splash PNGs + ${entries.length} startupImage entries (${BRAND_DARK}).`)
